# Tipo de dato int
from tkinter import Variable


x = 25
print(x)
print(type(x))


# Tipo de dato float
costo = 25.5
print(costo)
print(type(costo))

# Tipo de dato str
ciudad = "Santiago"
print(ciudad)
print(type(ciudad))

# Tipo de dato bool
x = True
print(x)
print(type(x))

# Cadena String
colorFavorite = "Azul"
print('Mi color favorito es: ' + colorFavorite)
print('Mi color favorito es: {}'.format(colorFavorite))
print(f'Mi color favorito es: {colorFavorite}')

# Tipos Booleanos
Variable = False
print(Variable)
Variable = 17 > 15
print(Variable)